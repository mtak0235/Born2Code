int		ft_atoi(const char *str)
{
	int	sign;
	int	num;

	while (*str == 32 || (9 <= *str && *str <= 13))
		str++;
	sign = 1;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	num = 0;
	while (48 <= *str && *str <= 57)
	{
		num *= 10;
		num = num + *str - 48;
		str++;
	}
	return (sign * num);
}
