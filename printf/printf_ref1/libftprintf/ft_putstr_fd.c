#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	len;

	if (s == NULL)
		return ;
	len = ft_strlen(s);
	while (len-- > 0)
	{
		write(fd, s, 1);
		s++;
	}
}
